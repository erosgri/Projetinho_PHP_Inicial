<div class="titulo">Estrutura de orientação de objetos</div>
<h2>1.) Classe:</h2>
<h3>Um molde ou modelo para criar objetos</h3>
<h3>Define atributos (dados) e métodos (ações)</h3>
<h2>2.) Objeto:</h2>
<h3>Uma instância de uma classe</h3>
<h3>É o que realmente existe e interage no programa</h3>
<h2>3.) Encapsulamento: </h2>
<h3>Esconde os detalhes internos de um objeto</h3>
<h3>Protege os dados usando modificadores de acesso (público, privado...)</h3>
<h3>Usa getters e setters para acessar ou modificar atributos, se necessário</h3>
<h2>4.) Herança:</h2>
<h3>Permite que uma classe filha herde características (métodos/atributos) de uma classe pai</h3>
<h2>5.) Polimorfismo:</h2>
<h3>Permite que diferentes classes implementem métodos com o mesmo nome, mas com comportamentos diferentes</h3>
<h2>6.) Abstração:</h2>
<h3>Foca apenas nos detalhes importantes, escondendo a complexidade</h3>
<h3>Ex: você usa ".falar()" sem saber como ele funciona internamente</h3>